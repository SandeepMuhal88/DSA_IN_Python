# what is Big O Notaion ?
# Big O notation is a mathematical concept used to describe the performance or complexity of an algorithm in
# terms of time or space required as the input size grows. It is often used to evaluate the efficiency of algorithms, 
# particularly in terms of:

# Time Complexity: How the running time of an algorithm increases with the input size.O()
# Space Complexity: How much memory or space is required by the algorithm as the input size grows.